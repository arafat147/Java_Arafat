package javaBasics;

public class UnaryOperator {

	public static void main(String[] args) {
		//Unary Operator
		//it is 2 type-
		//1. increment operator ++
			//is 2 type -
			//i. pre increment ++a
			//ii. post increment a++
				
		//2. decrement operator --
			//this is 2 type
			//i. pre decrement. --a
			//ii. post decrement. a--
		
		int a = 5;
		System.out.println(a);
		++a;
		System.out.println(a);
		a++;
		System.out.println(a);
		--a;
		System.out.println(a);
		a--;
		System.out.println(a);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}

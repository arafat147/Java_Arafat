package javaBasics;

public class EqualityAndRelationalOperator {
	
	// ==  is equal to
	// !=  is not equal to
	// >   is greater than
	// >=  is greater than or equal to
	// <   is less than
	// <=  is less than or equal to
	public static void main(String[] args) {
		
		int number = 70;
		// == operator
	    if(number==70) {
	    	System.out.println("number is rqual to 70");
	    }
		// != operator
	    if (number != 75) {
	    	System.out.println("number is not equal to 70 ");
	    }
	    
	    // > operator
	    if (number > 60) {
	    	System.out.println("number is greater than 60");
	    }
		// >= operator
		if(number>= 70) {
			System.out.println("number is greater trhan or equal to 70");
		}
		// < operator
		if(number < 80) {
			System.out.println("number is less than 80");
		}
		// <=
		if(number <=70) {
			System.out.println("number is less than or equal to 70");
		}
		
	}
    
    
	
	
	
	
	
	
	
	

}
